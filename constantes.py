"""
Este código imprime en la consola los valores de algunas
constantes matemáticas proporcionadas por el módulo math de Python. Aquí está la documentación
"""

import math

# Imprime el valor de pi
print("El valor de pi:", math.pi)

# Imprime el valor de e (número de Euler)
print("El valor de e:", math.e)

# Imprime el valor de infinito (∞)
print("El valor de infinito:", math.inf)

# Imprime el valor de un número indefinido (NaN - Not a Number)
print("El valor de un número indefinido:", math.nan)

